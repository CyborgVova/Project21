int	ft_strcmp(char *s1, char *s2)
{
	int s;
	int w;

	s = 0;
	w = 0;
	while (s1[s] != '\0' || s2[w] != '\0')
	{
		if (s1[s] != s2[w])
		{
			return (s1[s] - s2[w]);
		}
		s++;
		w++;
	}
	return (0);
}

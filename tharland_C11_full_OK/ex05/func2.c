int		plus(int a, int b)
{
	return (a + b);
}

int		minus(int a, int b)
{
	return (a - b);
}

int		multiply(int a, int b)
{
	return (a * b);
}

int		del(int a, int b)
{
	return (a / b);
}

int		ost(int a, int b)
{
	return (a % b);
}

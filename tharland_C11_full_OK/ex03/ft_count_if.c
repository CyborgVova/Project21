int		ft_count_if(char **tab, int lenght, int (*f)(char*))
{
	int i;
	int len;

	i = 0;
	len = 0;
	while (i < lenght)
	{
		if (f(tab[i]) != 0)
			len++;
		i++;
	}
	return (len);
}

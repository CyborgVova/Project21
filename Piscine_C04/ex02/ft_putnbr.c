#include <unistd.h>

void	ft_putnbr(int nb)
{
	int x;

	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	x = nb % 10 + 48;
	write(1, &x, 1);
}

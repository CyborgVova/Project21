#include "ft.h"

int		ft_check(char *str, int len)
{
	int i;

	i = 0;
	if (str[0] == '0' && len == 1)
		return (1);
	if (str[0] == '0' && len > 1)
		return (-1);
	while (i < len)
	{
		if (str[i] < '0' || str[i] > '9')
			return (-1);
		i++;
	}
	return (0);
}
